		     return null;
		} 
		
		
        foreach ($valueArray as $key=>$value) {

	         // a match was found!
		     if ($key == $searchKey) {
//                $valToLog = (is_array($value)) ? print_r($value, true) : $value; 		     	
//		     	$connection->logThis("findValueInArray: searchKey: $searchKey. value found: $valToLog");
